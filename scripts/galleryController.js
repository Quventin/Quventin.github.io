function nextImage() {
    var mainImage = document.getElementById('main-image');
    var subImages = document.getElementsByClassName('sub-image');
    var imageSrcParts = mainImage.src.split('/img/');
    var currentIndex = galleryImages.indexOf('img/' + imageSrcParts[1]);
    var nextIndex = currentIndex === (galleryImages.length - 1) ? 0 : currentIndex + 1;
    var nextIndexes = new Array();
    imageSrcParts = subImages[0].src.split('/img/');
    nextIndexes.push(galleryImages.indexOf('img/' + imageSrcParts[1]) === (galleryImages.length - 1) ? 0 : galleryImages.indexOf('img/' + imageSrcParts[1]) + 1);
    imageSrcParts = subImages[1].src.split('/img/');
    nextIndexes.push(galleryImages.indexOf('img/' + imageSrcParts[1]) === (galleryImages.length - 1) ? 0 : galleryImages.indexOf('img/' + imageSrcParts[1]) + 1);
    imageSrcParts = subImages[2].src.split('/img/');
    nextIndexes.push(galleryImages.indexOf('img/' + imageSrcParts[1]) === (galleryImages.length - 1) ? 0 : galleryImages.indexOf('img/' + imageSrcParts[1]) + 1);
    imageSrcParts = subImages[3].src.split('/img/');
    nextIndexes.push(galleryImages.indexOf('img/' + imageSrcParts[1]) === (galleryImages.length - 1) ? 0 : galleryImages.indexOf('img/' + imageSrcParts[1]) + 1);
    mainImage.src = galleryImages[nextIndex];
    subImages[0].src = galleryImages[nextIndexes[0]];
    subImages[1].src = galleryImages[nextIndexes[1]];
    subImages[2].src = galleryImages[nextIndexes[2]];
    subImages[3].src = galleryImages[nextIndexes[3]];
    mainImage.style.animation = 'none';
    mainImage.offsetHeight;
    mainImage.style.animation = null;

}

