function setImage(id) {
    var ids = new Array();
    ids.push(id);
    var i = 0;

    while (i < 4) {
        var lastId = ids[ids.length - 1];
        ids.push(lastId === galleryImages.length - 1 ? 0 : lastId + 1);
        i = i + 1;
    }
    document.getElementById('main-image').src = galleryImages[ids[0]];
    document.getElementById('first').src = galleryImages[ids[1]];
    document.getElementById('second').src = galleryImages[ids[2]];
    document.getElementById('third').src = galleryImages[ids[3]];
    document.getElementById('fourth').src = galleryImages[ids[4]];
}